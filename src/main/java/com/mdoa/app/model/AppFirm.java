package com.mdoa.app.model;
/**
 * 首页公司简介
 * @author Administrator
 *
 */
public class AppFirm {

	private String firmId;
	private String firmTitle;
	private String firmStockCode;
	private String firmRegistFund;
	private String firmYear;
	private String firmAbstract;
	private String sortId;
	private String videoUrl;
	public String getFirmId() {
		return firmId;
	}
	public void setFirmId(String firmId) {
		this.firmId = firmId;
	}
	public String getFirmStockCode() {
		return firmStockCode;
	}
	public void setFirmStockCode(String firmStockCode) {
		this.firmStockCode = firmStockCode;
	}
	public String getFirmRegistFund() {
		return firmRegistFund;
	}
	public void setFirmRegistFund(String firmRegistFund) {
		this.firmRegistFund = firmRegistFund;
	}
	public String getFirmYear() {
		return firmYear;
	}
	public void setFirmYear(String firmYear) {
		this.firmYear = firmYear;
	}
	public String getFirmAbstract() {
		return firmAbstract;
	}
	public void setFirmAbstract(String firmAbstract) {
		this.firmAbstract = firmAbstract;
	}
	public String getSortId() {
		return sortId;
	}
	public void setSortId(String sortId) {
		this.sortId = sortId;
	}
	public String getVideoUrl() {
		return videoUrl;
	}
	public void setVideoUrl(String videoUrl) {
		this.videoUrl = videoUrl;
	}
	public String getFirmTitle() {
		return firmTitle;
	}
	public void setFirmTitle(String firmTitle) {
		this.firmTitle = firmTitle;
	}
	
	

}
